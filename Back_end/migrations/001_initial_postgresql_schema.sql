CREATE TABLE IF NOT EXISTS users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  matricule TEXT UNIQUE NOT NULL,
  email TEXT,
  nom TEXT NOT NULL,
  post_nom TEXT NOT NULL,
  prenom TEXT NOT NULL,
  role TEXT NOT NULL DEFAULT 'etudiant',
  departement TEXT,
  promotion TEXT,
  password_hash TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE users ADD COLUMN IF NOT EXISTS email TEXT;

CREATE TABLE IF NOT EXISTS recours (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  course_code TEXT NOT NULL,
  course_title TEXT NOT NULL,
  evaluation_types TEXT NOT NULL,
  justification TEXT NOT NULL,
  proof_name TEXT,
  proof_path TEXT,
  assigned_professor_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
  professor_response TEXT,
  professor_decision TEXT,
  status TEXT NOT NULL DEFAULT 'pending',
  assigned_at TIMESTAMPTZ,
  treated_at TIMESTAMPTZ,
  published_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE recours ADD COLUMN IF NOT EXISTS proof_path TEXT;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS assigned_professor_id INTEGER REFERENCES users(id) ON DELETE SET NULL;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS professor_response TEXT;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS professor_decision TEXT;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS assigned_at TIMESTAMPTZ;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS treated_at TIMESTAMPTZ;
ALTER TABLE recours ADD COLUMN IF NOT EXISTS published_at TIMESTAMPTZ;

CREATE TABLE IF NOT EXISTS courses (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT UNIQUE NOT NULL,
  title TEXT NOT NULL,
  credits INTEGER,
  departement TEXT,
  teacher_name TEXT,
  promotions TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS admin_settings (
  key TEXT PRIMARY KEY,
  value TEXT,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_recours_user_id ON recours(user_id);
CREATE INDEX IF NOT EXISTS idx_recours_assigned_professor_id ON recours(assigned_professor_id);
CREATE INDEX IF NOT EXISTS idx_recours_status ON recours(status);
CREATE INDEX IF NOT EXISTS idx_courses_code ON courses(code);
